public class Animal {
    private int id;
    private String name;
    private String species;
    private int age;
    private int weight;

    public Animal(int id, String name, String species, int age, int weight) {
        this.id = id;
        this.name = name;
        this.species = species;
        this.age = age;
        this.weight = weight;
    }
}
