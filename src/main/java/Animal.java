public class Animal {
    private String id;
    private String species;
    private String name;
    private int age;
    private int weight;

    public Animal(String id, String species, String name, int age, int weight) {
        this.id = id;
        this.species = species;
        this.name = name;
        this.age = age;
        this.weight = weight;
    }
}
